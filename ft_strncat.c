#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	int		i;
	int		j;

	i = ft_strlen(s1);
	j = 0;
	while ((n > 0) && (s2[j] != '\0'))
	{
		s1[i] = s2[j];
		i++;
		j++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
