#include "libft.h"

void	ft_putnbr(int n)
{
	int		i;
	long	n2;

	n2 = n;
	if (n2 < 0)
	{
		ft_putchar('-');
		if (n2 == -2147483648)
			n2 = 2147483648;
		else
			n2 = n2 * (-1);
	}
	i = n2 % 10;
	if (n2 < 10)
		ft_putchar(i + '0');
	else
	{
		n2 = n2 / 10;
		ft_putnbr(n2);
		ft_putchar(i + '0');
	}
}
